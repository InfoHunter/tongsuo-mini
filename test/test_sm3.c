#include <string.h>
#include "test.h"
#include <tongsuo/minisuo.h>
#include "tongsuo/sm3.h"

/* the test is ported from tongsuo */
int test_sm3_hash(void)
{
    static const unsigned char input1[] = {
        0x61, 0x62, 0x63
    };

    /*
     * This test vector comes from Example 1 (A.1) of GM/T 0004-2012
     */
    static const unsigned char expected1[TSM_SM3_DIGEST_LEN] = {
        0x66, 0xc7, 0xf0, 0xf4, 0x62, 0xee, 0xed, 0xd9,
        0xd1, 0xf2, 0xd4, 0x6b, 0xdc, 0x10, 0xe4, 0xe2,
        0x41, 0x67, 0xc4, 0x87, 0x5c, 0xf2, 0xf7, 0xa2,
        0x29, 0x7d, 0xa0, 0x2b, 0x8f, 0x4b, 0xa8, 0xe0
    };

    static const unsigned char input2[] = {
        0x61, 0x62, 0x63, 0x64, 0x61, 0x62, 0x63, 0x64,
        0x61, 0x62, 0x63, 0x64, 0x61, 0x62, 0x63, 0x64,
        0x61, 0x62, 0x63, 0x64, 0x61, 0x62, 0x63, 0x64,
        0x61, 0x62, 0x63, 0x64, 0x61, 0x62, 0x63, 0x64,
        0x61, 0x62, 0x63, 0x64, 0x61, 0x62, 0x63, 0x64,
        0x61, 0x62, 0x63, 0x64, 0x61, 0x62, 0x63, 0x64,
        0x61, 0x62, 0x63, 0x64, 0x61, 0x62, 0x63, 0x64,
        0x61, 0x62, 0x63, 0x64, 0x61, 0x62, 0x63, 0x64
    };

    /*
     * This test vector comes from Example 2 (A.2) from GM/T 0004-2012
     */
    static const unsigned char expected2[TSM_SM3_DIGEST_LEN] = {
        0xde, 0xbe, 0x9f, 0xf9, 0x22, 0x75, 0xb8, 0xa1,
        0x38, 0x60, 0x48, 0x89, 0xc1, 0x8e, 0x5a, 0x4d,
        0x6f, 0xdb, 0x70, 0xe5, 0x38, 0x7e, 0x57, 0x65,
        0x29, 0x3d, 0xcb, 0xa3, 0x9c, 0x0c, 0x57, 0x32
    };

    unsigned char md1[TSM_SM3_DIGEST_LEN], md2[TSM_SM3_DIGEST_LEN];
    int ret = 0;

    if (tsm_sm3_oneshot(input1, sizeof(input1), md1) != TSM_OK)
        return TSM_FAILED;

    if (tsm_sm3_oneshot(input2, sizeof(input2), md2) != TSM_OK)
        return TSM_FAILED;

    ret = memcmp(md1, expected1, TSM_SM3_DIGEST_LEN);
    ASSERT_OK(ret);

    ret = memcmp(md2, expected2, TSM_SM3_DIGEST_LEN);
    ASSERT_OK(ret);

    return TSM_OK;
}

int main(void)
{
    TEST(test_sm3_hash);

    return TSM_OK;
}
